"""
Any e All

all() -> Retorna True se todos os elementos do iterável são verdadeiros ou ainda se o iterável está vazio.

any() -> Retorna True se qualquer elemento do iteravel for verdadeiroe. Se o iteravel estiver vazio, retorna false
"""

# Exemplo all()
print(all([0, 1, 2, 3, 4]))  # Todos os números são verdadeiros?true
print(all([ 1, 2, 3, 4]))  # Todos os números são verdadeiros?true
